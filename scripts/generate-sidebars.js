/**
 * 自动生成 sidebar.json 脚本
 * 
 * 功能：
 * 1. 扫描 docs 下所有分类目录中的书籍
 * 2. 解析 toc.md 获取完整的章节结构
 * 3. 为每本书生成一个 sidebar.json 文件
 * 
 * 使用方式：node scripts/generate-sidebars.js
 */

const fs = require('fs');
const path = require('path');

const DOCS_DIR = path.join(__dirname, '..', 'docs');

/**
 * 解析 toc.md 生成 Docusaurus sidebar 配置
 * @param {string} tocPath toc.md 文件路径
 * @param {string} bookPath book_zh 目录路径
 * @returns {Object} Docusaurus sidebar 配置
 */
function parseTocToSidebar(tocPath, bookPath) {
  const sidebar = [];
  
  if (!fs.existsSync(tocPath)) {
    console.log(`  ⚠️ toc.md 不存在: ${tocPath}`);
    // 返回自动生成配置作为备选
    return { mainSidebar: [{ type: 'autogenerated', dirName: '.' }] };
  }
  
  const content = fs.readFileSync(tocPath, 'utf-8');
  const lines = content.split('\n');
  
  let currentCategory = null;
  let hasPreface = false;
  let hasIndex = false;
  
  // 检查是否有序言和索引
  if (fs.existsSync(path.join(bookPath, 'preface.md'))) {
    hasPreface = true;
  }
  if (fs.existsSync(path.join(bookPath, 'index.md'))) {
    hasIndex = true;
  }
  
  // 添加首页（如果存在）
  if (hasIndex) {
    sidebar.push({
      type: 'doc',
      id: 'index',
      label: '📚 书籍首页'
    });
  }
  
  // 添加序言（如果存在）
  if (hasPreface) {
    sidebar.push({
      type: 'doc',
      id: 'preface',
      label: '序言'
    });
  }
  
  for (const line of lines) {
    // 匹配章节标题，如：### 第 1 部分: 设计概览 (Design Overview)
    // 或：## 第一部分：解析器基石
    const sectionMatch = line.match(/^#{2,3}\s+(.+)/);
    if (sectionMatch) {
      // 保存上一个 category
      if (currentCategory && currentCategory.items.length > 0) {
        sidebar.push(currentCategory);
      }
      
      const sectionTitle = sectionMatch[1].trim();
      
      // 提取中文部分（去掉英文括号部分）
      let label = sectionTitle
        .replace(/\s*\([^)]+\)\s*$/, '')  // 移除 (English) 部分
        .replace(/\s*（[^）]+）\s*$/, '') // 移除 （中文括号） 部分
        .trim();
      
      currentCategory = {
        type: 'category',
        label: label,
        collapsed: false,
        items: []
      };
      continue;
    }
    
    // 匹配章节链接，如：1. [目标与架构总览](overview/goals-and-architecture.md)
    // 或：1. [手势库的世界](./getting-started/introduction.md)
    const linkMatch = line.match(/^(\d+)\.\s*\[(.+)\]\((?:\.\/)?(.+\.md)\)/);
    if (linkMatch && currentCategory) {
      const docNumber = linkMatch[1].trim();  // 提取序号
      const docTitle = linkMatch[2].trim();
      let docPath = linkMatch[3].trim();
      
      // 移除 .md 扩展名，得到 doc id
      const docId = docPath.replace(/\.md$/, '');
      
      // 检查文件是否存在
      const fullPath = path.join(bookPath, docPath);
      if (fs.existsSync(fullPath)) {
        currentCategory.items.push({
          type: 'doc',
          id: docId,
          label: `${docNumber}. ${docTitle}`  // 添加序号到标签
        });
      }
    }
    
    // 匹配序言链接（不在章节内的）
    const prefaceMatch = line.match(/^-\s*\[(.+)\]\((?:\.\/)?preface\.md\)/);
    if (prefaceMatch && !currentCategory) {
      // 已经在上面处理了
    }
  }
  
  // 添加最后一个 category
  if (currentCategory && currentCategory.items.length > 0) {
    sidebar.push(currentCategory);
  }
  
  // 如果解析失败（没有任何章节），使用自动生成
  if (sidebar.length === 0 || (sidebar.length === 1 && sidebar[0].id === 'preface')) {
    console.log(`  ⚠️ toc.md 解析结果为空，使用自动生成配置`);
    return { mainSidebar: [{ type: 'autogenerated', dirName: '.' }] };
  }
  
  return { mainSidebar: sidebar };
}

/**
 * 处理单本书
 * @param {string} bookPath book_zh 目录路径
 */
function processBook(bookPath) {
  const bookName = path.basename(bookPath);
  console.log(`  📖 处理书籍: ${bookName}`);
  
  // 解析 toc.md 生成 sidebar
  const tocPath = path.join(bookPath, 'toc.md');
  const sidebar = parseTocToSidebar(tocPath, bookPath);
  
  // 写入 sidebar.json
  const sidebarPath = path.join(bookPath, 'sidebar.json');
  fs.writeFileSync(sidebarPath, JSON.stringify(sidebar, null, 2), 'utf-8');
  
  // 统计信息
  const categories = sidebar.mainSidebar.filter(item => item.type === 'category');
  const docs = sidebar.mainSidebar.filter(item => item.type === 'doc');
  const totalDocs = categories.reduce((sum, cat) => sum + (cat.items?.length || 0), 0) + docs.length;
  
  console.log(`  ✅ 生成 sidebar.json: ${categories.length} 个分类, ${totalDocs} 个文档`);
}

/**
 * 主函数
 */
function main() {
  console.log('🚀 开始生成 sidebar.json 文件...\n');
  console.log(`📁 文档目录: ${DOCS_DIR}`);
  
  // 获取所有分类目录
  const categories = fs.readdirSync(DOCS_DIR, { withFileTypes: true })
    .filter(item => item.isDirectory())
    .map(item => item.name);
  
  for (const category of categories) {
    const categoryPath = path.join(DOCS_DIR, category);
    console.log(`\n📂 分类: ${category}`);
    
    // 获取分类下的所有书籍
    const books = fs.readdirSync(categoryPath, { withFileTypes: true })
      .filter(item => item.isDirectory())
      .map(item => item.name);
    
    for (const book of books) {
      const bookPath = path.join(categoryPath, book);
      // 检查是否有 book_zh 子目录
      const bookZhPath = path.join(bookPath, 'book_zh');
      if (fs.existsSync(bookZhPath)) {
        processBook(bookZhPath);
      } else {
        processBook(bookPath);
      }
    }
  }
  
  console.log('\n\n✅ 完成！所有 sidebar.json 文件已生成。');
  console.log('   请运行 npm start 查看效果。');
}

main();
